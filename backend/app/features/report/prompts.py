# lang=='en' 일 때 service 가 원칙 5(언어)를 영어 지시로 치환할 때 쓰는 짝.
_KO_LANG_DIRECTIVE = "5. **언어**: 반드시 **한국어**로 품위 있게 작성하십시오."
_EN_LANG_DIRECTIVE = "5. **Language**: Write the entire report in polished, professional **English**."


def localize(prompt: str, lang: str) -> str:
    """lang=='en' 이면 프롬프트의 한국어 작성 지시를 영어 작성 지시로 치환한다."""
    if (lang or "ko").lower().startswith("en"):
        return prompt.replace(_KO_LANG_DIRECTIVE, _EN_LANG_DIRECTIVE)
    return prompt
